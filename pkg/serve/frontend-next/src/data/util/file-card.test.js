// file-card.test.js — run with `bun test`
import { expect, test } from 'bun:test';
import {
  parseFileCardData, isPreviewable, isHTMLPreviewable, iconKindFor, previewKind, looksBinary,
} from './file-card.js';

// ── parseFileCardData ────────────────────────────────────────────────────────

test('parses the JSON on the last line of a send_file result', () => {
  const result = 'Sent report.pdf to the user\n{"file_id":"f1","mime":"application/pdf","name":"report.pdf","size":1234,"url":"/api/files/f1"}';
  expect(parseFileCardData(result)).toEqual({
    file_id: 'f1', mime: 'application/pdf', name: 'report.pdf', size: 1234, url: '/api/files/f1',
  });
});

test('parses a single-line JSON result with no leading message', () => {
  const result = '{"name":"a.txt","size":10,"url":"/api/files/a"}';
  expect(parseFileCardData(result)).toEqual({ name: 'a.txt', size: 10, url: '/api/files/a' });
});

test('returns null when the last line is not valid JSON', () => {
  expect(parseFileCardData('just a plain message, no JSON here')).toBeNull();
  expect(parseFileCardData('line one\nline two, still not json')).toBeNull();
});

test('returns null for empty/missing result', () => {
  expect(parseFileCardData('')).toBeNull();
  expect(parseFileCardData(undefined)).toBeNull();
});

test('rejects a JSON payload whose url was not generated by our own API (sanity guard)', () => {
  expect(parseFileCardData('{"name":"a","size":1,"url":"https://evil.example/x"}')).toBeNull();
  expect(parseFileCardData('{"name":"a","size":1}')).toBeNull();
});

// ── isPreviewable / isHTMLPreviewable ────────────────────────────────────────

test('previewable by mime type', () => {
  expect(isPreviewable('x', 'image/png')).toBe(true);
  expect(isPreviewable('x', 'text/plain')).toBe(true);
  expect(isPreviewable('x', 'text/markdown')).toBe(true);
  expect(isPreviewable('x', 'text/html')).toBe(true);
  expect(isPreviewable('x', 'application/zip')).toBe(false);
});

test('previewable by extension when mime is missing/generic', () => {
  expect(isPreviewable('notes.md', undefined)).toBe(true);
  expect(isPreviewable('notes.markdown', 'application/octet-stream')).toBe(true);
  expect(isPreviewable('page.html', 'application/octet-stream')).toBe(true);
  expect(isPreviewable('page.htm', undefined)).toBe(true);
  expect(isPreviewable('archive.zip', undefined)).toBe(false);
});

test('mime with a charset parameter is still matched (only the media type before ";")', () => {
  expect(isPreviewable('x', 'text/plain; charset=utf-8')).toBe(true);
});

test('HTML-previewable only for html mime/extension', () => {
  expect(isHTMLPreviewable('x', 'text/html')).toBe(true);
  expect(isHTMLPreviewable('page.html', undefined)).toBe(true);
  expect(isHTMLPreviewable('page.htm', undefined)).toBe(true);
  expect(isHTMLPreviewable('notes.md', 'text/markdown')).toBe(false);
  expect(isHTMLPreviewable('x', 'image/png')).toBe(false);
});

// ── iconKindFor ───────────────────────────────────────────────────────────────

test('iconKindFor maps mime families to icon tokens', () => {
  expect(iconKindFor(undefined)).toBe('file');
  expect(iconKindFor('image/png')).toBe('image');
  expect(iconKindFor('text/plain')).toBe('text');
  expect(iconKindFor('application/pdf')).toBe('text');
  expect(iconKindFor('application/json')).toBe('text');
  expect(iconKindFor('application/zip')).toBe('archive');
  expect(iconKindFor('application/x-tar')).toBe('archive');
  expect(iconKindFor('application/octet-stream')).toBe('file');
});

// ── previewKind ───────────────────────────────────────────────────────────────

test('previewKind picks the FileViewer renderer', () => {
  expect(previewKind('a.png', 'image/png')).toBe('image');
  expect(previewKind('a.html', 'text/html')).toBe('html');
  expect(previewKind('a.htm', undefined)).toBe('html');
  expect(previewKind('a.md', 'text/markdown')).toBe('markdown');
  expect(previewKind('a.markdown', undefined)).toBe('markdown');
  expect(previewKind('a.txt', 'text/plain')).toBe('text');
  expect(previewKind('a.bin', 'application/octet-stream')).toBe('text');
});

// ── looksBinary ───────────────────────────────────────────────────────────────

test('looksBinary detects NUL bytes and heavy replacement-character density', () => {
  expect(looksBinary('hello world')).toBe(false);
  expect(looksBinary('a\0b')).toBe(true);
  const heavy = '\uFFFD'.repeat(20) + 'x';
  expect(looksBinary(heavy)).toBe(true);
  const light = 'a'.repeat(2000) + '\uFFFD';
  expect(looksBinary(light)).toBe(false);
});
